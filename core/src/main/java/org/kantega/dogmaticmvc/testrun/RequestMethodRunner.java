package org.kantega.dogmaticmvc.testrun;

import org.junit.internal.builders.AllDefaultPossibilitiesBuilder;
import org.junit.runner.Description;
import org.junit.runner.Runner;
import org.junit.runner.notification.RunNotifier;
import org.junit.runners.ParentRunner;
import org.junit.runners.model.InitializationError;

import java.util.List;

/**
*
*/
public class RequestMethodRunner extends ParentRunner<Runner> {
    private List<Runner> runners;

    public RequestMethodRunner(Class<?> testClass) throws InitializationError {
        super(RequestMethodRunner.class);
        this.runners = new AllDefaultPossibilitiesBuilder(false).runners(RequestMethodRunner.class, new Class[]{testClass});
    }

    @Override
    protected List<Runner> getChildren() {
        return runners;
    }

    @Override
    protected Description describeChild(Runner child) {
        return child.getDescription();
    }

    @Override
    protected void runChild(Runner runner, final RunNotifier notifier) {
        runner.run(notifier);
    }
}
